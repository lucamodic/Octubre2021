package unlam.edu.ar.pb2;

public class Figurita {

	String codigo;
	String grupo;
	String seleccion;
	String nombre;
	Integer valor;
	
	public Figurita(String codigo, String grupo, String seleccion, String nombre, Integer valor) {
		
		this.codigo = codigo;
		this.grupo = grupo;
		this.seleccion = seleccion;
		this.nombre = nombre;
		this.valor = valor;
		
	}

}
